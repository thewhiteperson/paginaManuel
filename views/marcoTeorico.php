        <h2>APOYO EMOCIONAL EN SITUACIONES DE CRISIS, AUTOESTIMA Y AUTOACEPTACIÓN</h2>

        <div>
            <div>
                <h4>1. Conceptualización de crisis emocional</h4>
                <p>
                    Una crisis emocional se entiende como un estado temporal de desequilibrio psicológico que ocurre cuando una persona enfrenta un evento o situación que percibe como abrumadora y que supera sus recursos habituales de afrontamiento.
                </p>
            </div>

            <div>
                <h4>2. Escucha activa como base del apoyo emocional</h4>
                <p>
                    La escucha activa constituye uno de los pilares fundamentales del apoyo emocional. Este concepto fue ampliamente desarrollado dentro del enfoque humanista por Carl Rogers, quien planteó que para brindar ayuda efectiva es necesario escuchar con empatía, aceptación incondicional y sin emitir juicios.
                </p>
                <p>La escucha activa implica:</p>
                <ul>
                    <li>Prestar atención plena a la persona.</li>
                    <li>No interrumpir ni minimizar la experiencia emocional.</li>
                </ul>
            </div>

            <div>
                <h4>3. Validación emocional y compañía</h4>
                <p>La validación emocional consiste en reconocer y aceptar los sentimientos de la persona como legítimos, aunque no se compartan.</p>
            </div>

            <div>
                <h4>4. Acciones prácticas y fortalecimiento del afrontamiento</h4>
                <p>El apoyo emocional también incluye intervenciones prácticas orientadas a restablecer el equilibrio. Desde la perspectiva cognitivo-conductual, propuesta por Aaron T. Beck, establecer metas pequeñas y alcanzables contribuye a recuperar la sensación de control.</p>
                <p>Entre las acciones prácticas se encuentran:</p>
                <ul>
                    <li>Fomentar rutinas básicas de sueño y alimentación.</li>
                    <li>Establecer actividades diarias estructuradas.</li>
                </ul>
            </div>

            <div>
                <h4>5. Evitar la revictimización</h4>
                <p>En situaciones de crisis, especialmente cuando existe trauma, es fundamental evitar la revictimización. Esto implica no presionar a la persona para que relate detalles dolorosos ni cuestionar su experiencia.</p>
            </div>

            <div>
                <h4>6. Importancia del apoyo profesional</h4>
                <p>Aunque el apoyo social es fundamental, en ciertos casos resulta necesaria la intervención profesional.</p>
            </div>
        </div>

        <div>
            <h2>Autoestima y Autoaceptación</h2>

            <div>
                <h4>1. Conceptualización de la autoestima</h4>
                <p>La autoestima se define como la valoración que una persona realiza de sí misma, incluyendo creencias, emociones y actitudes hacia su propio valor. Según Morris Rosenberg, la autoestima es una actitud global positiva o negativa hacia el yo.</p>
            </div>

            <div>
                <h4>2. Autoaceptación como base del bienestar psicológico</h4>
                <p>
                    La autoaceptación se refiere a la capacidad de reconocer y aceptar tanto las cualidades positivas como las limitaciones personales sin caer en la auto desvalorización.
                </p>
            </div>

            <div>
                <h4>3. Autocompasión</h4>
                <p>La autocompasión consiste en tratarse a uno mismo con amabilidad y comprensión en momentos de dificultad o fracaso. Este concepto ha sido ampliamente estudiado por Kristin Neff, quien plantea que la autocompasión incluye tres componentes principales:</p>
                <ol>
                    <li>1. Amabilidad hacia uno mismo, en lugar de autocrítica severa.</li>
                    <li>2. Humanidad compartida, reconociendo que el sufrimiento y el error son experiencias comunes.</li>
                    <li>3. Atención plena (mindfulness), que permite observar las emociones sin exagerarlas ni reprimirlas</li>
                </ol>
            </div>

            <div>
                <h4>4. Identificación de fortalezas personales</h4>
                <p>Reconocer fortalezas, logros y cualidades personales es una estrategia clave para fortalecer la autoestima.</p>
            </div>

            <div>
                <h4>5. Mentalidad de crecimiento</h4>
                <p>El concepto de mentalidad de crecimiento fue desarrollado por Carol Dweck, quien distingue entre mentalidad fija y mentalidad de crecimiento.</p>
                <ul>
                    <li>La mentalidad fija considera que las habilidades y capacidades son inmutables.</li>
                    <li>La mentalidad de crecimiento sostiene que las habilidades pueden desarrollarse mediante esfuerzo y aprendizaje</li>
                </ul>
            </div>

            <div>
                <h4>
                    6. Autocuidado y bienestar integral
                </h4>
                <p>El autocuidado comprende prácticas orientadas a preservar la salud física y mental, tales como mantener una alimentación equilibrada, realizar actividad física, dormir adecuadamente y dedicar tiempo al descanso.</p>
            </div>

            <div>
                <h4>7. Aceptación compasiva</h4>
                <p>La aceptación compasiva implica reconocer y aceptar emociones, pensamientos y características personales sin negarlos ni juzgarlos severamente.</p>
            </div>
        </div>
